<p><font color="white">The Farmer's portal is a new initiative founded by the Govt. of India to support poor farmers cultivate their land and provide resources to them.Farmers who cannot cultivate their land list it in the farmer’s portal, which provides details of the land. The farmer’s portal helps farmers who cannot cultivate their crop in the field. This portal helps not only the farmer but also the associates and man power provided which helps create employment opportunities. Once the land is listed the associates from the nearest agricultural college collect data of the soil. The main features of this system include information retrieval from the soil which provides statistical data of mineral concentration and chemical composition of the soil. Based on these statistics, the crops to be cultivated are decided and the work power required to cultivate the land is decided based on the area of land. Once the statistical data is calculated the crops suitable for the soil are selected and agriculture is performed with maximum efficiency. Once the agriculture is done, the profit margin from the crop is shared between the government and the farmer. The proposed system can be achieved through a web application with a SQL database using embedded SQL queries at the back end to maintain the land information. This system not only helps the farmers from losses but also helps the government to provide employment opportunities and to create agricultural crops with maximum quality and efficiency. This also helps the government to tackle calamities like droughts and floods. 
